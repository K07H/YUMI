const std::size_t Base64::encodedSize(std::size_t n)
{
    return 4 * ((n + 2) / 3);
}

const std::size_t Base64::decodedSize(std::size_t n)
{
    return n / 4 * 3;
}

std::size_t Base64::b64Encode(void* dest, const void* src, std::size_t len)
{
    char* out = static_cast<char*>(dest);
    const char* in = static_cast<const char*>(src);
    const char* tab = Base64::b64GetAlphabet();

    std::size_t n = len / 3;
    while (n--)
    {
        *out++ = tab[(in[0] & 0xfc) >> 2];
        *out++ = tab[((in[0] & 0x03) << 4) + ((in[1] & 0xf0) >> 4)];
        *out++ = tab[((in[2] & 0xc0) >> 6) + ((in[1] & 0x0f) << 2)];
        *out++ = tab[in[2] & 0x3f];
        in += 3;
    }

    switch (len % 3)
    {
    case 2:
        *out++ = tab[(in[0] & 0xfc) >> 2];
        *out++ = tab[((in[0] & 0x03) << 4) + ((in[1] & 0xf0) >> 4)];
        *out++ = tab[(in[1] & 0x0f) << 2];
        *out++ = '=';
        break;

    case 1:
        *out++ = tab[(in[0] & 0xfc) >> 2];
        *out++ = tab[((in[0] & 0x03) << 4)];
        *out++ = '=';
        *out++ = '=';
        break;

    case 0:
        break;
    }

    return out - static_cast<char*>(dest);
}

std::pair<std::size_t, std::size_t> Base64::b64Decode(void* dest, const char* src, std::size_t len)
{
    char* out = static_cast<char*>(dest);
    const unsigned char* in = reinterpret_cast<const unsigned char*>(src);
    unsigned char c3[3], c4[4] = { 0, 0, 0, 0 };
    int i = 0;
    int j = 0;

    const signed char* inverse = Base64::b64GetInverse();

    while (len-- && *in != '=')
    {
        const signed char v = inverse[*in];
        if (v == -1)
            break;
        ++in;
        c4[i] = v;
        if(++i == 4)
        {
            c3[0] = (c4[0] << 2) + ((c4[1] & 0x30) >> 4);
            c3[1] = ((c4[1] & 0xf) << 4) + ((c4[2] & 0x3c) >> 2);
            c3[2] = ((c4[2] & 0x3) << 6) + c4[3];

            for (i = 0; i < 3; i++)
                *out++ = c3[i];
            i = 0;
        }
    }

    if (i)
    {
        c3[0] = (c4[0] << 2) + ((c4[1] & 0x30) >> 4);
        c3[1] = ((c4[1] & 0xf) << 4) + ((c4[2] & 0x3c) >> 2);
        c3[2] = ((c4[2] & 0x3) << 6) + c4[3];

        for (j = 0; j < i - 1; j++)
            *out++ = c3[j];
    }

    return { out - static_cast<char*>(dest), in - reinterpret_cast<const unsigned char*>(src) };
}
